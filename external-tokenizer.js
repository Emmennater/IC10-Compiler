import { ExternalTokenizer } from "@lezer/lr";
import { ChannelColon, Colon } from "./parser_lang.terms.js"; // generated by lezer-generator

const colon = 58; // ":"

// Required to differentiate between labels and channels
export const colonTokenizer = new ExternalTokenizer((input) => {
  if (input.next != colon) return;
  const isDigit = input.peek(1) >= 48 && input.peek(1) <= 57;
  input.acceptToken(isDigit ? ChannelColon : Colon, 1);
});